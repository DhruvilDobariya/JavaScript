// In JavaScript two types of data type.
// 1) Primitive data type :
// i) Numbers :
    var number1 = 7;
    var number2 = 10.2;
    console.log(number1, number2);
// ii) Strings :
    var str1 = "This is a string";
    var str2 = 'This is also string';
    console.log(str1);
    console.log(str2);
// iii) Booleans :
    var a = true;
    var b = false;
    console.log(a, b);
// iv) Undefined :
    var y = undefined;
    var z;
    console.log(y, z);
// v) Null :
    var d = null;
    console.log(d);
// vi) Symbol :
// Symbol is advanced topic of JavaScrit.

// 2) Reference data type :
// i) Array :
    var n = [2, 4.5, "Dhruvil", 7, true];
    console.log(n);
    console.log(n[0]);
    console.log(n[1]);
    console.log(n[2]);
    console.log(n[3]);
    console.log(n[4]);
// ii) Object :
    var rollNo = {
        Dhruvil: 1,
        Jenil: 2,
        Jash: 3
    };
    console.log(rollNo);
    console.log(rollNo.Dhruvil);
    console.log(rollNo.Jenil);
    console.log(rollNo.Jash);